using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Methods
{
	public class Calculator
	{
		// Instance method
		public int Multiply(int a, int b)
		{
			return a * b;
		}

		// Static method
		public static int Subtract(int a, int b)
		{
			return a - b;
		}
	}
}
